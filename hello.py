print("bitwise 연산")
print(2 & 1, 1 & 0, 0 & 1, 0 & 0) # AND
# 2 = 10
# 1 = 01
# &-----
# 0 = 00
print(1 | 1, 1 | 0, 0 | 1, 0 | 0) # OR
print(2 ^ 1, 1 ^ 0, 0 ^ 1, 0 ^ 0) # XOR
# 2 = 10
# 1 = 01
# ^-----
# 3 = 11

print("논리 연산")
print(2 and 1, 1 and 0, 0 and 1, 0 and 0) # AND
print(1 or 1, 1 or 0, 0 or 1, 0 or 0) # OR
print(1 != 1, 1 != 0, 0 != 1, 0 != 0) # XOR